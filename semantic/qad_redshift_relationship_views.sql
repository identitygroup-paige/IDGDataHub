CREATE SCHEMA IF NOT EXISTS DATA_VALIDATION.SEMANTIC;

CREATE OR REPLACE VIEW DATA_VALIDATION.SEMANTIC."V_ESTIMATE_WITH_SARASOTA_ESTIMATE_LINE_QAD" AS
SELECT
    p."SOURCE",
    p."CAMPUS",
    p."LAST_HARMONIZED_UPDATE",
    p."ESTIMATE_NUMBER",
    p."CUST_NAME",
    p."EST_DESCRIPTION",
    p."PO_NUMBER",
    p."SALESPERSON",
    p."ESTIMATOR",
    p."ESTIMATE_DATE",
    p."ESTIMATED_LABOR_COST",
    p."ESTIMATED_MATERIAL_COST",
    p."ESTIMATED_OVERHEAD",
    p."PRICE",
    p."PRODUCT_TYPE",
    p."SOD_ACCT",
    p."WRK_CUSTDATE",
    c."SOURCE" AS "SARASOTA_ESTIMATE_LINE_SOURCE",
    c."CAMPUS" AS "SARASOTA_ESTIMATE_LINE_CAMPUS",
    c."ESTIMATE_NUMBER" AS "SARASOTA_ESTIMATE_LINE_ESTIMATE_NUMBER",
    c."CUST_NAME" AS "SARASOTA_ESTIMATE_LINE_CUST_NAME",
    c."PRODUCT_TYPE" AS "SARASOTA_ESTIMATE_LINE_PRODUCT_TYPE",
    c."PRICE" AS "SARASOTA_ESTIMATE_LINE_PRICE",
    c."ESTIMATE_DATE" AS "SARASOTA_ESTIMATE_LINE_ESTIMATE_DATE",
    'IDG_ESTIMATE_QAD' AS _SEMANTIC_PARENT_TABLE,
    'IDG_SARASOTA_ESTIMATE_LINE_QAD' AS _SEMANTIC_CHILD_TABLE,
    'ESTIMATE_NUMBER' AS _SEMANTIC_PARENT_COLUMN,
    'ESTIMATE_NUMBER' AS _SEMANTIC_CHILD_COLUMN
FROM DATA_VALIDATION.RAW_REDSHIFT."IDG_ESTIMATE_QAD" p
LEFT JOIN DATA_VALIDATION.RAW_REDSHIFT."IDG_SARASOTA_ESTIMATE_LINE_QAD" c
  ON NULLIF(TRIM(TO_VARCHAR(p."ESTIMATE_NUMBER")), '') =
     NULLIF(TRIM(TO_VARCHAR(c."ESTIMATE_NUMBER")), '')
 AND NULLIF(TRIM(TO_VARCHAR(p."ESTIMATE_NUMBER")), '') IS NOT NULL
 AND NULLIF(TRIM(TO_VARCHAR(c."ESTIMATE_NUMBER")), '') IS NOT NULL;

